    private void bt_logActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_logActionPerformed
        String username = tf_email.getText();
        String plainPassword = tf_pass.getText();

        try {
            // Buat objek MessageDigest dengan algoritma MD5
            MessageDigest md = MessageDigest.getInstance("MD5");

            // Ubah kata sandi yang dimasukkan menjadi byte array
            byte[] passwordBytes = plainPassword.getBytes();

            // Hitung hash MD5 dari byte array kata sandi
            byte[] passwordHashBytes = md.digest(passwordBytes);

            // Ubah byte array hash MD5 menjadi format string hexadecimal
            StringBuilder sb = new StringBuilder();
            for (byte b : passwordHashBytes) {
                sb.append(String.format("%02x", b));
            }

            // Dapatkan kata sandi dalam format MD5
            String passwordMD5 = sb.toString();

            // Dapatkan ID pengguna dari database menggunakan metode getUserID
            int userID = getUserID(username, passwordMD5);

            if (userID != -1) {
//                System.out.println("user= "+userID);
                // Jika data pengguna ditemukan, buka jendela main_GUI dengan userID
                dispose();
                Dashboard mainpage = new Dashboard(userID);
                mainpage.show();
            } else {
                JOptionPane.showMessageDialog(this, "Email atau password tidak terdaftar");
                lb_judul.setText(plainPassword); // Ini hanya untuk debug, Anda dapat menghapusnya
                tf_email.setText("");
                tf_pass.setText("");
            }
        } catch (HeadlessException | NoSuchAlgorithmException e) {
        }
    }//GEN-LAST:event_bt_logActionPerformed
    private void showPasswordInput() {
        // Membuat JTextField untuk memasukkan password
        JPasswordField passwordField = new JPasswordField(20);

        // Membuat panel untuk menampilkan JTextField
        JPanel passwordPanel = new JPanel();
        passwordPanel.add(new JLabel("Masukkan password:"));
        passwordPanel.add(passwordField);

        // Menampilkan panel dan meminta input password
        int result = JOptionPane.showConfirmDialog(null, passwordPanel, "Konfirmasi Password", JOptionPane.OK_CANCEL_OPTION);
        statusPasswordInput = true; // Set statusPasswordInput menjadi true ketika password input aktif

        if (result == JOptionPane.OK_OPTION) {
            // Gantilah "passwordAnda" dengan password yang benar
            String passwordBenar = "admin";
            char[] inputPassword = passwordField.getPassword();

            // Periksa apakah password yang dimasukkan benar
            if (new String(inputPassword).equals(passwordBenar)) {
                // Jika password benar, ubah ukuran jendela
                if (getExtendedState() != fbox.Start.MAXIMIZED_BOTH) {
                    setExtendedState(Start.MAXIMIZED_BOTH);
                } else {
                    setExtendedState(Start.NORMAL);
                }
                statusPasswordInput = false;
            } else {
                // Jika password salah, tampilkan pesan kesalahan
                JOptionPane.showMessageDialog(null, "Password salah!", "Error", JOptionPane.ERROR_MESSAGE);
                statusPasswordInput = false;
            }
        } else if (result == JOptionPane.CANCEL_OPTION) {
            statusPasswordInput = false; // Atur statusPasswordInput menjadi false
            // Tutup dialog
        }
    }

