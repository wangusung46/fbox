    public int getUserID(String username, String password) {
//        int userID = -1; // Nilai default jika data tidak ditemukan

        try (Connection con = koneksi.ConfigDB.conect()) {
            Statement stm = con.createStatement();
            String sql = "SELECT * FROM users WHERE email='" + username + "' AND password='" + password + "'";
            ResultSet rs = stm.executeQuery(sql);

            // Jika data ditemukan, isi userID dengan nilai dari database
            if (rs.next()) {
                userID = rs.getInt("id_users");
            }

        } catch (SQLException e) {
        }

        return userID;
    }

